import SwiftUI

extension Font {
    static func avenirMedium(_ size: CGFloat) -> Font {
        .custom("AvenirNext-Medium", size: size)
    }

    static func avenirDemiBold(_ size: CGFloat) -> Font {
        .custom("AvenirNext-DemiBold", size: size)
    }
}
